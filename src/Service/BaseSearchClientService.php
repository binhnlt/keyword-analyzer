    /**
     * @return string|null
     */
    public function getKeyword() : ?string
    {
        return $this->keyword;
    }

    /**
     * @param string $keyword
     * @return self
     */
    public function setKeyword(string $keyword) : self
    {
        $this->keyword = $keyword;
        
        return $this;
    }

    /**
     * Get query string for the request
     *
     * @return array
     */
    protected abstract function getQueryString(): array;

    public function __construct()
    {
        $this->logger = new Logger('BaseSearchClientService');

        if (!$this->requestClient) {
            $this->requestClient = new Client([
                'cookies' => true,
                'headers' => $this->getHeaderConfiguration(),
            ]);
        }
    }

    /**
     * Do search
     *
     * @return Response
     */
    public function doSearch()
    {
        $queryString = $this->getQueryString();
        $this->logger->info(__FUNCTION__ . ' - query string data');
        $this->logger->info(json_encode($queryString));

        $response = $this->requestClient->request('GET', $this->getSearchRequestUrl(), [
            'query' => $queryString
        ]);

        return $response;
    }

    /**
     * Get header configuration
     *
     * @return array
     */
    protected function getHeaderConfiguration()
    {
        return [
            'User-Agent' => $this->getRandomUserAgent(),
